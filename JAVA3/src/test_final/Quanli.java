    void showClick2(int row) {
        if (row < 0) {
            return;
        }
        if (model.getRowCount() <= 0) {
            them();
            return;
        }

        txtma.setText(model.getValueAt(row, 0).toString());
        txttieude.setText(model.getValueAt(row, 1).toString());
        txtloai.setText(model.getValueAt(row, 2).toString());
        txtnam.setText(model.getValueAt(row, 3).toString());
        
        

    }
    



