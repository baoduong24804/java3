    void luu() {
        boolean ch = false;
        Connection con = null;
        Statement st = null;
        ResultSet rs = null;
        PreparedStatement pst = null;

        if (txtma.getText().trim().isEmpty() || txtloai.getText().trim().isEmpty() || txtnam.getText().trim().isEmpty() || txttieude.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Khong duoc de trong");
            return;
        }

        try {
            con = Ketnoi.Ketnoisql.conn();
            st = con.createStatement();
            rs = st.executeQuery("Select Ma from testfinal");
            while (rs.next()) {
                if (rs.getString("Ma").equalsIgnoreCase(txtma.getText())) {
                    ch = true;
                    break;
                }
            }
            if (ch) {
                JOptionPane.showMessageDialog(null, "Trùng mã");
                return;
            }
            pst = con.prepareStatement("Insert into testfinal values(?,?,?,?)");
            pst.setString(1, txtma.getText());
            pst.setString(2, txttieude.getText());
            pst.setString(3, txtloai.getText());
            pst.setInt(4, Integer.parseInt(txtnam.getText()));
            if (pst.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(null, "Them thanh cong");
                return;
            }
            
            st.close();
            pst.close();
            con.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    void xoa(){
        if (txtma.getText().trim().isEmpty()){
        JOptionPane.showMessageDialog(null, "Không được để trống mã");
            return;
        }
            try {
            Connection con = Ketnoi.Ketnoisql.conn();
            PreparedStatement pst = con.prepareStatement("Delete from testfinal where Ma = ?");
            pst.setString(1, txtma.getText());
            if (pst.executeUpdate() > 0) {
                loaddata();
                return;
            }
            JOptionPane.showMessageDialog(null, "Khong co ma sv: " + txtma.getText());

        } catch (Exception e) {
            e.printStackTrace();

        }
    }
    
